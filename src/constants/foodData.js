/** Nutrient keys to aggregate */
const _NUTRIENT_KEYS = [
  'cal', 'pro', 'carb', 'fat', 'fib', 'sug',
  'sod', 'pot', 'cal_m', 'iron', 'mag',
  'vitC', 'vitD', 'vitB12', 'folate', 'vitA',
];

/**
 * Calculate nutrition for `qty` servings of the food with id `fid`.
 * Returns an object with every nutrient value multiplied by qty,
 * plus `n` (name) and `fid` for convenience.
 */
function calcEntry(fid, qty) {
  const food = FOOD_DB[fid];
  if (!food) return null;
  const result = { fid, n: food.n };
  _NUTRIENT_KEYS.forEach(k => {
    result[k] = Math.round(((food[k] || 0) * qty) * 10) / 10;
  });
  return result;
}

/**
 * Sum nutrition across an array of meal entries
 * Each entry: { fid, qty, note? }
 */
function calcMeal(entries) {
  const totals = {};
  _NUTRIENT_KEYS.forEach(k => { totals[k] = 0; });
  if (!entries || !entries.length) return totals;
  entries.forEach(e => {
    const vals = calcEntry(e.fid, e.qty);
    if (!vals) return;
    _NUTRIENT_KEYS.forEach(k => {
      totals[k] += vals[k] || 0;
    });
  });
  // round to 1 decimal
  _NUTRIENT_KEYS.forEach(k => {
    totals[k] = Math.round(totals[k] * 10) / 10;
  });
  return totals;
}

/**
 * Sum nutrition across all meals (array of meal objects).
 * Each meal: { id, name, ico, entries: [...] }
 */
function calcTotal(meals) {
  const totals = {};
  _NUTRIENT_KEYS.forEach(k => { totals[k] = 0; });
  if (!meals || !meals.length) return totals;
  meals.forEach(meal => {
    const mealTotals = calcMeal(meal.entries);
    _NUTRIENT_KEYS.forEach(k => {
      totals[k] += mealTotals[k] || 0;
    });
  });
  _NUTRIENT_KEYS.forEach(k => {
    totals[k] = Math.round(totals[k] * 10) / 10;
  });
  return totals;
}

